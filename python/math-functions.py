import math

pi =   3.14 
x = 1
y = 2 
z = 3


#print(round(pi)) rounds down to closest number
print(math.ceil(pi)) #rounds down to 4 higher end
print(math.floor(pi)) #rounds down to lower end so 3
print(abs(pi)) # gives how far the number is from zero, its absolute value 
print(pow(pi, 2)) # raises the power to given number
print(math.sqrt(420)) #find square root 
print(max(x,y,z)) #find max value
print(min(x,y,z)) # find the min value 